    $(document).ready(function() {
        $('#login-form-modal').submit(function(e) {
            e.preventDefault();
            $.ajax({
                url : "?mod=login&act=login",
                method : "post",
                data : $(this).serialize(),
                success : function(res) {
                    var data = JSON.parse(res);
                    if (data === false) {
                        $('#error-m i').text('Đăng nhập thất bại!!');
                    } else {
                        $('#error-m i').text('');
                        window.location.reload();
                    }
                }, error : function(err) {console.log(res);}
            })
           })
        $(document).on('click', '#logout', function() {
            $.ajax({
                url : "?mod=logout",
                method : "get",
                success : function(res) {
                    if (JSON.parse(res) === true) {
                        window.location.reload();
                    }
                }
            })
        })
        $(document).on('click', '.site li', function(){
            var code = $(this).children().attr('slug-code');
            $.ajax({
                url : '?mod=set-site',
                type : 'post',
                data : {
                    code : code
                },
                success : function(res) {
                    if(res) {
                        window.location.reload();
                    }
                }
            })
        })
    })
</script>