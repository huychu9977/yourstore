<script>
    $(document).ready(function() {
        if(localStorage.getItem('cart') === null) {
            localStorage.setItem('cart', JSON.stringify([]));
        } else {
            var cart = JSON.parse(localStorage.getItem('cart'));
            $('#open-cart .badge--cart').text(cart.length);
        }
        function showCart() {
            var cart = JSON.parse(localStorage.getItem('cart'));
            $('#open-cart .badge--cart').text(cart.length);
            $('.cart__total span').text('0 ');
            if(cart.length > 0) {
                $('#cart-item').children().remove();
                var total = 0;
                cart.forEach(function(item) {
                    total += item.product.price * item.quantity;
                    $('#cart-item').append(
                        '<li class="cart__item">'
                            +'<div class="cart__item__image pull-left">'
                                +'<a href="#"><img src="../upload/'+item.product.image+'" alt="" /></a>'
                            +'</div>'
                            +'<div class="cart__item__control">'
                                +'<div slug-code="'+item.product.code+'" class="cart__item__delete"><a href="javascript:void(0)" class="icon icon-delete"><span>Delete</span></a></div>'
                                +'<div slug-code="'+item.product.code+'" class="cart__item__edit"><a href="javascript:void(0)" class="icon icon-edit"><span>Edit</span></a></div>'
                            +'</div>'
                            +'<div class="cart__item__info">'
                                +'<div class="cart__item__info__title">'
                                    +'<h2><a href="#">'+item.product.name+'</a></h2>'
                                +'</div>'
                                +'<div class="cart__item__info__price"><span class="info-label">Price:</span><span>'+fomatVND(item.product.price)+'</span></div>'
                                +'<div class="cart__item__info__qty"><span class="info-label">Qty:</span>'
                                    +'<input type="text" class="input--ys" value="'+item.quantity+'" />'
                                +'</div>'
                            +'</div>'
                        +'</li>'
                        );
                });
                $('.cart__total span').text(fomatVND(total));
            }else{
                $('#cart-item').append('Giỏ hàng đang trống!');
            }

        }
        $('#open-cart').click(function(e) {
            e.preventDefault();
            showCart();
            $('#open-cart').dropdown('toggle');
        });
        $(document).on('click', '.cart__item__delete', function(){
            var code = $(this).attr('slug-code');
            var cart = JSON.parse(localStorage.getItem('cart'));
            cart.forEach(function(item, index){
                if(item.product.code === code) {
                    cart.splice(index, 1);
                }
            });
            localStorage.setItem('cart', JSON.stringify(cart));
            showCart();
        });
        $(document).on('click', '.cart__item__edit', function(){
            var quantity = $(this).parents('.cart__item').find('input.input--ys').val();
            var code = $(this).attr('slug-code');
            var cart = JSON.parse(localStorage.getItem('cart'));
            cart.forEach(function(item, index){
                if(item.product.code === code) {
                    item.quantity = parseInt(quantity);
                }
            });
            localStorage.setItem('cart', JSON.stringify(cart));
            showCart();
        });
        function fomatVND(input) {
            return input.toLocaleString('it-IT', {style : 'currency', currency : 'VND'});
        }
    })
</script>